function displayresults(query, k=10)
	@time R = search(index, query, SimilaritySearch.getknnresult(k))
	L = []
	for (i, (id, d)) in enumerate(R.res)
		word = index[id]
		push!(L, @htl """
		<tr><td>$i</td><td>$(word)</td><td>$id</td><td>$(round(d, digits=3)) </td><td><a href="https://en.wiktionary.org/wiki/$(word)" target="_blank">meaning</a></td><td><button onclick='var elem = document.querySelector("bond >input"); elem.value = "$word"; elem.dispatchEvent(new CustomEvent("input")); '>similar</button></td></tr>
		""")
	end
	
	@htl """

<h2>Query</h2>
query: $(query), n=$(length(index))
<table>
<tr><td>knn</td><td>word</td><td>id</td><td>dist</td><td>wiktionary</td><td></td></tr>
$L
</table>

	"""
end

